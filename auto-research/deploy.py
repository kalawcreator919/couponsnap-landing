#!/usr/bin/env python3
"""
deploy.py — LoopLab Auto-Research Pipeline Step 4
Generate GitHub Actions workflow to auto-deploy challenger variants.
Also creates the deployment manifest and variant index page.
"""

import json
import os
from datetime import datetime
from pathlib import Path

DATA_DIR = Path(__file__).parent / "data"
VARIANTS_DIR = Path(__file__).parent / "variants"
REPO_ROOT = Path(__file__).parent.parent  # assumes auto-research/ is inside repo

GITHUB_REPO = os.environ.get("GITHUB_REPO", "kalawcreator919/couponsnap-landing")
GITHUB_PAGES_BASE = f"https://{GITHUB_REPO.split('/')[0]}.github.io/{GITHUB_REPO.split('/')[1]}"


def load_variants_summary() -> dict:
    summary_file = DATA_DIR / "variants_summary.json"
    if not summary_file.exists():
        raise FileNotFoundError("variants_summary.json not found. Run generate.py first.")
    with open(summary_file, encoding="utf-8") as f:
        return json.load(f)


def generate_github_actions_workflow(variants: list) -> str:
    """Generate GitHub Actions workflow YAML for deploying variants."""
    variant_deploy_steps = ""
    for v in variants:
        vtype = v["variant_type"]
        variant_deploy_steps += f"""
      - name: Deploy variant-{vtype}
        run: |
          mkdir -p challengers/{vtype}
          cp auto-research/variants/variant_{vtype}.html challengers/{vtype}/index.html
          echo "Deployed variant-{vtype}"
"""

    return f"""# Auto-generated by LoopLab Auto-Research Pipeline
# DO NOT EDIT MANUALLY - regenerated on each pipeline run
name: Deploy CRO Challengers

on:
  workflow_dispatch:
    inputs:
      run_pipeline:
        description: 'Run full pipeline before deploying'
        required: false
        default: 'false'
  push:
    branches: [main]
    paths:
      - 'auto-research/variants/**'
      - '.github/workflows/deploy-challengers.yml'

permissions:
  contents: write
  pages: write
  id-token: write

jobs:
  deploy-challengers:
    runs-on: ubuntu-latest
    steps:
      - name: Checkout repository
        uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: '3.11'

      - name: Run pipeline (if requested)
        if: github.event.inputs.run_pipeline == 'true'
        env:
          ANTHROPIC_API_KEY: ${{{{ secrets.ANTHROPIC_API_KEY }}}}
          GA4_PROPERTY_ID: ${{{{ secrets.GA4_PROPERTY_ID }}}}
          GA4_CREDENTIALS: ${{{{ secrets.GA4_CREDENTIALS }}}}
        run: |
          cd auto-research
          python harvest.py
          python evaluate.py
          python generate.py
          python deploy.py
{variant_deploy_steps}
      - name: Copy variant manifests
        run: |
          cp auto-research/data/variants_summary.json challengers/manifest.json

      - name: Generate variant index
        run: |
          python auto-research/deploy.py --generate-index

      - name: Commit and push challengers
        run: |
          git config user.name "LoopLab AutoResearch Bot"
          git config user.email "noreply@looplab.ai"
          git add challengers/
          git diff --cached --quiet || git commit -m "chore: deploy CRO challengers [$(date -u +%Y-%m-%d)]

Co-Authored-By: Paperclip <noreply@paperclip.ing>"
          git push

  verify-deployment:
    needs: deploy-challengers
    runs-on: ubuntu-latest
    steps:
      - name: Wait for GitHub Pages
        run: sleep 30

      - name: Verify variant pages
        run: |
          for variant in headline cta layout; do
            url="{GITHUB_PAGES_BASE}/challengers/$variant/"
            status=$(curl -sk -o /dev/null -w "%{{http_code}}" "$url")
            echo "variant-$variant: HTTP $status"
            [ "$status" = "200" ] || echo "WARNING: $variant returned $status"
          done
"""


