def create_deployment_manifest(variants: list, summary: dict) -> dict:
    """Create deployment manifest for tracking active experiments."""
    return {
        "deployment_id": f"deploy_{datetime.utcnow().strftime('%Y%m%d_%H%M%S')}",
        "deployed_at": datetime.utcnow().isoformat() + "Z",
        "base_url": f"{GITHUB_PAGES_BASE}/",
        "control_url": f"{GITHUB_PAGES_BASE}/",
        "challengers": [
            {
                "variant_id": v.get("variant_id", f"v_{v['variant_type']}"),
                "variant_type": v["variant_type"],
                "url": f"{GITHUB_PAGES_BASE}/challengers/{v['variant_type']}/",
                "hypothesis": v.get("hypothesis", ""),
                "status": "active",
                "deployed_at": datetime.utcnow().isoformat() + "Z",
            }
            for v in variants
        ],
        "ga4_events_to_track": [
            "cta_click",
            "how_it_works_click",
            "blog_click",
            "variant_view",
        ],
        "success_metric": "cta_click (chrome webstore)",
        "run_duration_days": 7,
    }


def main():
    import sys

    # Check if called with --generate-index flag (from GitHub Actions)
    generate_index_only = "--generate-index" in sys.argv

    summary = load_variants_summary()
    variants = summary.get("variants", [])

    if not variants:
        print("[deploy] No variants found in summary. Run generate.py first.")
        return

    # Create challengers directory structure
    challengers_dir = REPO_ROOT / "challengers"
    challengers_dir.mkdir(exist_ok=True)

    if not generate_index_only:
        # Generate GitHub Actions workflow
        workflow_dir = REPO_ROOT / ".github" / "workflows"
        workflow_dir.mkdir(parents=True, exist_ok=True)
        workflow_path = workflow_dir / "deploy-challengers.yml"
        workflow_content = generate_github_actions_workflow(variants)
        with open(workflow_path, "w", encoding="utf-8") as f:
            f.write(workflow_content)
        print(f"[deploy] GitHub Actions workflow → {workflow_path}")

    # Generate variant index page
    index_html = generate_variant_index(variants, summary)
    index_path = challengers_dir / "index.html"
    with open(index_path, "w", encoding="utf-8") as f:
        f.write(index_html)
    print(f"[deploy] Variant index page → {index_path}")

    # Create deployment manifest
    manifest = create_deployment_manifest(variants, summary)
    manifest_path = challengers_dir / "manifest.json"
    with open(manifest_path, "w", encoding="utf-8") as f:
        json.dump(manifest, f, ensure_ascii=False, indent=2)
    print(f"[deploy] Deployment manifest → {manifest_path}")

    if not generate_index_only:
        # Save deployment record
        deploy_record_path = DATA_DIR / "deployment.json"
        with open(deploy_record_path, "w", encoding="utf-8") as f:
            json.dump(manifest, f, ensure_ascii=False, indent=2)

        print(f"\n[deploy] Deployment ready!")
        print(f"[deploy] Control:    {GITHUB_PAGES_BASE}/")
        for c in manifest["challengers"]:
            print(f"[deploy] Challenger: {c['url']}")
        print(f"\n[deploy] Next: commit + push to trigger GitHub Actions workflow")
        print(f"[deploy]   git add -A && git commit -m 'chore: CRO challengers deploy'")
        print(f"[deploy]   git push")

    return manifest


if __name__ == "__main__":
    main()
